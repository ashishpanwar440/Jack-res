    private void spawn(int x, int y, boolean isLeft){
        int i = hei*y/jPanel1.getHeight();
        int j = wid*x/jPanel1.getWidth();
        if(isLeft){
            if(jTextField4.getText().equals("")){
                currentMove[i][j] = true;
            }else{
                String[] dot = jTextField4.getText().substring(1).split("\\},\\{");
                boolean[][] spawny = new boolean[hei][wid];
                for(int k = 0; k < dot.length; k++){
                    int ii = Integer.parseInt(dot[k].substring(1, dot[k].indexOf("\"", 1)));
                    String forJ = dot[k].substring(dot[k].indexOf("[") + 1, dot[k].indexOf("]"));
                    String[] individual = forJ.split(",");
                    for(int l = 0; l < individual.length; l++){
                        if(ii < hei && Integer.parseInt(individual[l]) < wid )
                            spawny[ii][Integer.parseInt(individual[l])] = true;
                    }
                }
                if(jRadioButton1.isSelected()){
                    for(int ii = 0; ii < spawny.length;ii++){
                        for(int jj = 0; jj < spawny[0].length;jj++){
                            if(i+ii < hei && j+jj < wid && spawny[ii][jj]) currentMove[i+ii][j+jj] = true;
                        }
                    }
                }
                if(jRadioButton2.isSelected()){
                    for(int ii = 0; ii < spawny.length;ii++){
                        for(int jj = 0; jj < spawny[0].length;jj++){
                            if(i-jj >= 0 && j+ii < wid && spawny[ii][jj]) currentMove[i-jj][j+ii] = true;
                        }
                    }
                }
                if(jRadioButton3.isSelected()){
                    for(int ii = 0; ii < spawny.length;ii++){
                        for(int jj = 0; jj < spawny[0].length;jj++){
                            if(i-ii >= 0 && j-jj >= 0 && spawny[ii][jj]) currentMove[i-ii][j-jj] = true;
                        }
                    }
                }
                if(jRadioButton4.isSelected()){
                    for(int ii = 0; ii < spawny.length;ii++){
                        for(int jj = 0; jj < spawny[0].length;jj++){
                            if(i+jj < hei && j-ii >= 0 && spawny[ii][jj]) currentMove[i+jj][j-ii] = true;
                        }
                    }
                }
            }
        }else{
            currentMove[i][j] = false;
        }
        
        repain();
        
    }
    
