    private void repain(){
        buff.setColor(jPanel1.getBackground());
        buff.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
        for(int i = 0 ; i < hei ; i++){
            for(int j = 0 ; j < wid; j++){
                if(currentMove[i][j]){
                    buff.setColor(Color.YELLOW);
                    int x = j * jPanel1.getWidth()/wid;
                    int y = i * jPanel1.getHeight()/hei;
                    buff.fillRect(x, y, jPanel1.getWidth()/wid, jPanel1.getHeight()/hei);
                }
            }
        }
        if(wid < 300 && hei < 300){
            for(int i = 1; i < wid;i++){
                int x = jPanel1.getWidth() * i / wid;
                buff.setColor(Color.BLACK);
                if(i % 10 == 0)buff.setColor(Color.white);
                buff.drawLine(x, 0, x, jPanel1.getHeight());
            }
            for(int i = 1; i < hei;i++){
                int y = jPanel1.getHeight() * i / hei;
                buff.setColor(Color.BLACK);
                if(i % 10 == 0)buff.setColor(Color.white);
                buff.drawLine(0, y, jPanel1.getWidth(), y);
            }
        }
        jPanel1.getGraphics().drawImage(buffer, 0, 0, jPanel1);
    }
    
