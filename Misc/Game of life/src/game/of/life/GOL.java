    boolean play;
    Image buffer;
    Graphics buff;
    int interval;
    int tim = 0;
    Timer time = new Timer();
    TimerTask task = new TimerTask(){
        public void run(){
            if(play){
                tim++;
                if(tim>=interval){
                    tim = 0;
                    for(int i = 0;i < hei;i++){
                        for(int j = 0;j < wid;j++){
                            nextMove[i][j] = decide(i,j);
                        }
                    }
                    for(int i = 0;i < hei;i++){
                        for(int j = 0;j < wid;j++){
                            currentMove[i][j] = nextMove[i][j];
                        }
                    }
                    repain();
                }
            }
        }
    };
    
