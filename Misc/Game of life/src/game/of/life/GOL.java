    private void set(){
        if(wid != Integer.parseInt(jTextField1.getText()) ||
                hei != Integer.parseInt(jTextField2.getText())){
            wid = Integer.parseInt(jTextField1.getText());
            hei = Integer.parseInt(jTextField2.getText());
            currentMove = new boolean[hei][wid];
            nextMove = new boolean[hei][wid];
        }
        interval = Integer.parseInt(jTextField3.getText());
        repain();
    }
    
